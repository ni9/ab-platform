package com.intuit.wasabi.api;

import org.junit.Test;

import java.util.ArrayList;
import java.util.List;

import static org.hamcrest.MatcherAssert.assertThat;
import static org.hamcrest.collection.IsEmptyCollection.empty;
import static org.hamcrest.core.Is.is;

public class ComponentHealthListTest {

    @Test
    public void setVersion() throws Exception {
        List<ComponentHealth> componentHealths = new ArrayList<>();
        ComponentHealthList chList = new ComponentHealthList(componentHealths);

        assertThat(chList.getComponentHealths(), is(empty()));

        chList.setVersion("v1.0");

        assertThat(chList.getVersion(), is("v1.0"));
    }
}
